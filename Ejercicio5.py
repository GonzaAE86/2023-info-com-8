num1 = int(input("Ingrese el primer número entero: "))
num2 = int(input("Ingrese el segundo número entero: "))

cociente = num1 // num2
resto = num1 % num2

print("El cociente de la división de", num1, "entre", num2, "es:", cociente)
print("El resto de la división de", num1, "entre", num2, "es:", resto)
