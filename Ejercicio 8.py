pi = 3.14159

radio = float(input("Ingrese el radio del círculo: "))

diametro = 2 * radio
circunferencia = 2 * pi * radio
area = pi * radio ** 2

print("El diámetro del círculo de radio", radio, "es:", diametro)
print("La circunferencia del círculo de radio", radio, "es:", circunferencia)
print("El área del círculo de radio", radio, "es:", area)
